package binary;

public class DoubleEx {

	public static void main(String[] args) {
		
		double dNum = 3.14;
		float fNum = 3.14F;
		
		System.out.println(dNum);
		System.out.println(fNum);
		
		var vNum = 3.14;
		var num = 3;
		
		System.out.println(vNum);
		System.out.println(num);

	}

}
